    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String name = "ESPRESSO";
        String qty = JOptionPane.showInputDialog(this, "Enter Quantity", 1);
        double quantity = Double.parseDouble(qty);
        try {
            String sqlquery = "SELECT * FROM item_list WHERE ITEM_DESCRIPTION LIKE '%" + name + "%'";
            pst = con.prepareStatement(sqlquery);
            rs = pst.executeQuery();
            while (rs.next()) {
                double price = rs.getDouble(3);
                double total = price * quantity;
                String[] data = {name, qty, String.valueOf(price), String.valueOf(total)};
                DefaultTableModel model = (DefaultTableModel) table_receipt.getModel();
                model.addRow(data);
                double total_overall = 0.0;
                for (int i = 0; i < model.getRowCount(); i++) {
                    total_overall += Double.parseDouble(table_receipt.getValueAt(i, 3).toString());
                    String result = String.format("%.2f", total_overall);
                    txt_total.setText(result);
                }               
            }
        } catch (Exception e) {
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed


    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        try{
            DefaultTableModel model = (DefaultTableModel) table_receipt.getModel();
            for (int i=0;i<model.getRowCount();i++){
                String customer = txt_customer.getText();
                String item = (String)table_receipt.getModel().getValueAt(i, 0);
                String quantity = (String)table_receipt.getModel().getValueAt(i, 1);
                String price = (String)table_receipt.getModel().getValueAt(i, 2);
                double qty = Double.parseDouble(quantity);
                double p = Double.parseDouble(price);
                String total_price = (String)table_receipt.getModel().getValueAt(i, 3);
                double tp = Double.parseDouble(total_price);
                String sqlquery = "INSERT INTO coffee_transaction (customer_name,ITEM_DESCRIPTION,ITEM_QUANTITY,"
                        + "ITEM_PRICE,ITEM_TOTAL_PRICE) VALUES (?,?,?,?,?)";
                pst = con.prepareStatement(sqlquery);
                pst.setString(1, customer);
                pst.setString(2, item);
                pst.setDouble(3, qty);
                pst.setDouble(4, p);
                pst.setDouble(5, tp);
                pst.execute();
                new Payment().show();
                
            }    
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton18ActionPerformed

